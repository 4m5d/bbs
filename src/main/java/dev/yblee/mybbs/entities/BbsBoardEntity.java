package dev.yblee.mybbs.entities;

public class BbsBoardEntity {
    private int index;
    private String id;
    private String name;

    public BbsBoardEntity() {
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
